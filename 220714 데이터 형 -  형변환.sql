-- HIRE_DATE가 1987년 6월인 직원
SELECT * FROM employees 
WHERE HIRE_DATE BETWEEN '1987/06/01' AND  '1987/06/30';

-- 날짜 덧셈뺄셈
SELECT ADDDATE('1987-06-01',41);
SELECT SUBDATE('1987-06-01', 1);
SELECT DATE_ADD('1987-06-01', INTERVAL 40 DAY);
SELECT DATE_SUB('1987-06-01', INTERVAL 1 MONTH);
SELECT '1987-06-01' + INTERVAL 1 YEAR;
SELECT '1987-06-30' + INTERVAL 1 DAY;

-- 요일 / 1 - 일요일
SELECT DAYOFWEEK('2022-07-14');

SELECT EXTRACT(YEAR FROM '2022-07-14'); -- 년도 추출
SELECT EXTRACT(MONTH FROM '2022-07-14'); -- 월 추출
SELECT EXTRACT(DAY FROM '2022-07-14'); -- 일 추출

SELECT DATEDIFF('2022-06-01', '2022-06-13');

SELECT STR_TO_DATE('2021/06/13', '%Y/%m/%d'); -- 문자열을 날짜로바꿔주는
SELECT DATE_FORMAT(DATE '2021-06-13', '%Y/%M/%D'); -- 날짜를 문자열로 바꿔주는

-- 직원 테이블(17일에 고용된 직원)
SELECT * FROM employees;
SELECT * FROM employees WHERE (SELECT EXTRACT(DAY FROM HIRE_DATE)) = 17;

-- JOB_HISTORY 각 행별로 일한 '기간' (일)을 
SELECT * FROM job_history;
SELECT START_DATE, END_DATE, DATEDIFF(END_DATE, START_DATE) AS "기간" FROM job_history;


